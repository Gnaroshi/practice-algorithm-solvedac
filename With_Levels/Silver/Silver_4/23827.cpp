int main(void) {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  ll n, t, cur = 0, ans = 0, md = 1000000007;
  cin >> n;
  for (int i = 0; i < n; i++) {
    cin >> t;
    ans += (cur * t);
    cur += t;
    ans %= md;
  }

  cout << ans << '\n';

  return 0;
}
