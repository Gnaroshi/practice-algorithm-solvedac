  cin.tie(nullptr);

  int n, mn, mx;
  cin >> n;
  vi v(n * 3);
  for (auto &i : v) {
    cin >> i;
  }

  sort(v.begin(), v.end());

  mn = v[n];
  mx = v[n + n - 1];
  cout << mx - mn << '\n';

