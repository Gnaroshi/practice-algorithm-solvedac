int main(void) {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int tc;
  cin >> tc;
  while (tc--) {
    int l, r, s;
    cin >> l >> r >> s;
    if (r - s <= s - l)
      cout << (r - s) * 2;
    else
      cout << (s - l) * 2 + 1;
    cout << '\n';
  }

  return 0;
}
