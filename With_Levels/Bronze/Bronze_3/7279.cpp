int main(void) {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n, k, a, b, ans = 0, cur = 0;
  cin >> n >> k;
  while (n--) {
    cin >> a >> b;
    cur += (a - b);
    ans = max(ans, cur - k);
  }
  cout << ans << '\n';

  return 0;
}
